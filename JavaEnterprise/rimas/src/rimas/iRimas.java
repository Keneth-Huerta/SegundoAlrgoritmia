    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        in++;

        if (in < 12) {

            try {
                imagen = ImageIO.read(new File("src/imagenes/" + nums[in] + ".jpg")).getScaledInstance(jLabel1.getWidth(), jLabel1.getHeight(), 0);
            } catch (IOException e) {
                System.out.println("Imagen no encontrada");
            }

            jLabel1.setIcon(new ImageIcon(imagen));
        } else {
            if (JOptionPane.showInputDialog(null, "Quieres seguir jugando").equalsIgnoreCase("si")) {
                nums = rim.rand();
                in = 0;
                valor=0;
            } else {
                System.exit(0);
            }

        }
        
        valor = jProgressBar1.getValue() + (100 / 13);
        jProgressBar1.setValue(valor);
    }//GEN-LAST:event_jButton2ActionPerformed

